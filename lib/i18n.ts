const DATETIME_LOCALE_MAP: Record<string, string> = {
  en: "en-US",
  zh: "zh-CN",
  de: "de-DE",
  fr: "fr-FR",
  es: "es-ES",
  it: "it-IT",
  pt: "pt-PT",
  ja: "ja-JP",
  ko: "ko-KR",
  ru: "ru-RU",
  ar: "ar-SA"
};

function normalizeLanguageCode(code: string): string {
  return code.trim().toLowerCase();
}

function fillParams(template: string, params?: I18nParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (full, key: string) => {
    const value = params[key];
    return value === undefined ? full : String(value);
  });
}

export function getDateTimeLocale(languageCode: string): string {
  const normalized = normalizeLanguageCode(languageCode);
  return DATETIME_LOCALE_MAP[normalized] ?? DATETIME_LOCALE_MAP.en;
}

export function translate(languageCode: string, key: I18nKey, params?: I18nParams): string {
  const normalized = normalizeLanguageCode(languageCode);
  const selected = MESSAGES[normalized] ?? EN_MESSAGES;
  return fillParams(selected[key], params);
}

export function createTranslator(languageCode: string) {
  return (key: I18nKey, params?: I18nParams): string => translate(languageCode, key, params);
}
